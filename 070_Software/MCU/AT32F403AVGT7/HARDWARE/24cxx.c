//在AT24CXX指定地址读出一个数据
//ReadAddr:开始读数的地址  
//返回值  :读到的数据
u8 AT24CXX_ReadOneByte(u16 ReadAddr)
{
	u8 temp = 0;
	u8 nErr = 1;
	IIC_Start();

	//
	if (EE_TYPE > AT24C16)
	{
		//发送写命令
		nErr &= IIC_Write(0XA0);

		//IIC_Wait_Ack();
		nErr &= IIC_Write(ReadAddr >> 8);//发送高地址

		//IIC_Wait_Ack();
	}
	else
	{
		//发送器件地址0XA0,写数据
		nErr &= IIC_Write(0XA0 + ((ReadAddr / 256) << 1));
	}

	//IIC_Wait_Ack();
	//发送低地址
	nErr &= IIC_Write(ReadAddr % 256);

	//IIC_Wait_Ack();
	IIC_Start();

	//进入接收模式
	nErr &= IIC_Write(0xA1);

	//IIC_Wait_Ack();
	temp = IIC_Read(0);

	//产生一个停止条件
	IIC_Stop();

	if (nErr != 1)
	{
		//警告些错误
		//LED_ToggleHighLED(2, LED_BLINK);
	}
	else
	{
		////LED_ToggleHighLED(2, LED_OFF);
	}

	return temp;
}

//在AT24CXX指定地址写入一个数据
//WriteAddr  :写入数据的目的地址    
//DataToWrite:要写入的数据
void AT24CXX_WriteOneByte(u16 WriteAddr, u8 DataToWrite)
{
	u8 nErr = 1;
	WC_DISABLE;
	delay_ms(1);

	IIC_Start();

	if (EE_TYPE > AT24C16)
	{
		//发送写命令
		nErr&=IIC_Write(0xA0);
		//IIC_Wait_Ack();

		//发送高地址
		nErr &= IIC_Write(WriteAddr >> 8);
	}
	else
	{
		//发送器件地址0xA0,写数据 
		nErr &= IIC_Write(0XA0 + ((WriteAddr / 256) << 1));
	}

	//IIC_Wait_Ack();
	//发送低地址
	nErr &= IIC_Write(WriteAddr % 256);

	//IIC_Wait_Ack();
	//发送字节
	nErr &= IIC_Write(DataToWrite);

	//IIC_Wait_Ack();
	//产生一个停止条件 
	IIC_Stop();

	delay_ms(5);

	if (nErr != 1)
	{
		//警告些错误
		//LED_ToggleHighLED(2, LED_BLINK);
	}
	else
	{
		////LED_ToggleHighLED(2, LED_OFF);
	}

	WC_ENABLE;
}

//在AT24CXX里面的指定地址开始写入长度为Len的数据
//该函数用于写入16bit或者32bit的数据.
//WriteAddr  :开始写入的地址  
//DataToWrite:数据数组首地址
//Len        :要写入数据的长度2,4
void AT24CXX_WriteLenByte(u16 WriteAddr, u32 DataToWrite, u8 Len)
{
	u8 t;

	for (t=0; t<Len; t++)
	{
		AT24CXX_WriteOneByte(WriteAddr + t, (DataToWrite >> (8 * t)) & 0xFF);
	}
}

